        public MainWindow()
        {
            InitializeComponent();
            
            
            int icount = 0;
            while (icount < 50)
            {
                grid1.ColumnDefinitions.Add(new ColumnDefinition());
                grid1.RowDefinitions.Add(new RowDefinition());
                icount++;
            }
            grid1.Width = 600;
            grid1.Height = 600;
            grid1.HorizontalAlignment = HorizontalAlignment.Left;
            grid1.VerticalAlignment = VerticalAlignment.Top;
            grid1.ShowGridLines = true;
            grid1.Background = new SolidColorBrush(Colors.LightSteelBlue);
            myArrayCopy(room, roomreset, 50, 50);
            reset();
            


        }

        private void myArrayCopy(int[,] s,int[,] d,int rows,int cols)
        {
            for (int i = 0; i < rows; i++)
            {
                for (int j = 0; j < cols; j++)
                {
                    d[i, j] = s[i, j];
                }
            }
        }
            private void reset()
        {
            grid1.Children.Clear();
            myArrayCopy(roomreset, room,50,50);
            for (int i = 0; i < 50; i++)
            {
                for (int j = 0; j < 50; j++)
                {
                    Button b = new Button();
                    b.Click += (b_Click);
                    b.Background = Brushes.Gray;
                    if (room[i, j] == 1)
                    {
                        b.Background = Brushes.Blue;
                    }
                    Grid.SetRow(b, i);
                    Grid.SetColumn(b, j);
                    grid1.Children.Add(b);
                }
            }
        }
        private void ResetClick(object sender, RoutedEventArgs e)
        {
            reset();
        }
        private async void CleanClick(object sender, RoutedEventArgs e)
        {
            rd1.IsEnabled = false;
            rd2.IsEnabled = false;
            clean.IsEnabled = false;
            resetroom.IsEnabled = false;
            Button lastB = null;
            myrobot = new robot(robotx, roboty, 0);
            myrobot.room = this.room; // pass in only for api move to detect if it is blocked 
            myrobot.cleanroom();
            movetimes.Text = "movetimes:" + myrobot.movetimes.ToString();
            lefttimes.Text = "lefttimes:" + myrobot.lefttimes.ToString();
            righttimes.Text = "righttimes:" + myrobot.righttimes.ToString();
            cleantimes.Text = "cleantimes:" + myrobot.cleantimes.ToString();
            //show path
            foreach (string a in myrobot.allpath)
            {
                string[] two = a.Split(',');
                int x1 = Convert.ToInt32(two[0]);
                int y1 = Convert.ToInt32(two[1]);
                UIElement btn = GetChildren(grid1, x1, y1);
                Button b = (Button)btn;
                b.Background = Brushes.Green;

                if (lastB != null) lastB.Background = Brushes.White;
                lastB = b;
                await Task.Delay(TimeSpan.FromSeconds(0.2));
            }

            rd1.IsEnabled = true;
            rd2.IsEnabled = true;
            clean.IsEnabled = true;
            resetroom.IsEnabled = true;
        }
        private static UIElement GetChildren(Grid grid, int row, int column)
        {
            foreach (UIElement child in grid.Children)
            {
                if (Grid.GetRow(child) == row
                      &&
                   Grid.GetColumn(child) == column)
                {
                    return child;
                }
            }
            return null;
        }


        private void b_Click(object sender, EventArgs e)
        {
            if (!rd1.IsEnabled && !rd2.IsEnabled) return;
            var buttonClicked = sender as Button;
            var i = (int)buttonClicked.GetValue(Grid.RowProperty);
            var j = (int)buttonClicked.GetValue(Grid.ColumnProperty);
            if ((i == 0) || (i == 49) || (j == 0) || (j == 49)) return;

            if (rd1.IsChecked == true)
            {
                if (buttonClicked.Background != Brushes.Green)
                {
                    if (room[i, j] == 0) buttonClicked.Background = Brushes.Blue;
                    if (room[i, j] == 1) buttonClicked.Background = Brushes.Gray;
                    room[i, j] = (room[i, j] + 1) % 2;
                }
            }

            if (rd2.IsChecked == true && buttonClicked.Background == Brushes.Gray)
            {
                if (oldRobot != null) oldRobot.Background = Brushes.Gray;
                oldRobot = buttonClicked;
                robotx = i; //row dirrection is x
                roboty = j; //col dirrection is y
                buttonClicked.Background = Brushes.Green;
            }

        }

